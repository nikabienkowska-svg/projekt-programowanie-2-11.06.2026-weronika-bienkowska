df = eeg_data.frame

# Zmienna celu (Class) to oryginalnie tekst ('1' i '2').
# Dla wygody modeli klasyfikacyjnych zamieniamy to na liczby całkowite: 0 (otwarte oczy) i 1 (zamknięte oczy).
